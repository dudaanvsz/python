#Escreva um programa que leia 6 notas diferentes e faça a média do aluno
nota1 = float(input('Digite uma nota : '))
nota2 = float(input('Digite uma segunda nota : '))
nota3 = float(input('Digite uma terceira nota : '))
nota4 = float(input('Digite uma  quarta nota : '))
nota5 = float(input('Digite uma quinta nota : '))
nota6 = float(input('Digite uma sexta nota : '))

média = nota1 + nota2 + nota3 + nota4 + nota5 + nota6
print(f'A sua média final é {média / 6}: ')